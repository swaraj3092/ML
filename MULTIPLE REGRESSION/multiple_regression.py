import pandas as pd

df = pd.read_excel("/content/sample_data/Book1.xlsx")

x1 = list(df.iloc[:, 0])
x2 = list(df.iloc[:, 1])
y  = list(df.iloc[:, 2])

n = len(x1)

sx1 = sum(x1)
sx2 = sum(x2)
sy = sum(y)

sx1x1 = sum(a*a for a in x1)
sx2x2 = sum(a*a for a in x2)
sx1x2 = sum(x1[i]*x2[i] for i in range(n))
sx1y = sum(x1[i]*y[i] for i in range(n))
sx2y = sum(x2[i]*y[i] for i in range(n))

def det3(a11,a12,a13,a21,a22,a23,a31,a32,a33):
    return (
        a11*(a22*a33 - a23*a32)
        - a12*(a21*a33 - a23*a31)
        + a13*(a21*a32 - a22*a31)
    )

D = det3(
    n,    sx1,   sx2,
    sx1,  sx1x1, sx1x2,
    sx2,  sx1x2, sx2x2
)

Da = det3(
    sy,   sx1,   sx2,
    sx1y, sx1x1, sx1x2,
    sx2y, sx1x2, sx2x2
)

Db1 = det3(
    n,    sy,    sx2,
    sx1,  sx1y,  sx1x2,
    sx2,  sx2y,  sx2x2
)

Db2 = det3(
    n,    sx1,   sy,
    sx1,  sx1x1, sx1y,
    sx2,  sx1x2, sx2y
)

a  = Da / D
b1 = Db1 / D
b2 = Db2 / D

print("a =", a)
print("b1 =", b1)
print("b2 =", b2)

x1_new = 3
x2_new = 2

y_pred = a + b1 * x1_new + b2 * x2_new
print("Predicted y =", y_pred)

